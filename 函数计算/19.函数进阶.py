

# 案例1:计算n的阶乘
# 递归调用(先层层递进，再逐层回归)：指的是在函数中自己调用自己的情况--->一定得终结点

"""
jc(10)=10 * jc(9)
jc(9) =9 * jc(8)
jc(8)=8*jc(7)
jc(7)=7*jc(6)=7*720=5040
jc(6)=6*jc(5)=6*120=720
jc(5)=5*jc(4)=5*24=120
jc(4)=4*jc(3)=4*6=24
jc(3)=3*jc(2)=3*2=6
jc(2)=2*jc(1)=2*1=2
jc(1)=1
"""

def jc(n):
    if n == 1:
        return 1
    else:
        return n * jc(n-1)

